const mongoose = require("mongoose");

// Define schema for admin
const adminSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Create Admin model
const Admin = mongoose.model("Admin", adminSchema);

module.exports = Admin;
